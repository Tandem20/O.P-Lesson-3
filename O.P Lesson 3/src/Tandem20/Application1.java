package Tandem20;

public class Application1 {
	public static void main(String[] args) {
		String a = "Четверг";
		switch (a ) {
			case "Вівторок" : {
				System.out.println(" Навчання");
				break;
			}
			case "Понеділок" : {
				System.out.println(" Робота");
				break;
			}
			case "Середа" : {
				System.out.println("Фітнес");
				break;
			}
			case "Четверг" : {
				System.out.println(" Відрядження");
				break;
			}
			case "П'ятниця" : {
				System.out.println(" Здати звіт");
				break;
			}
			case "Субота" : {
				System.out.println(" Відвідати батьків");
				break;
			}
			case "Неділя" : {
				System.out.println(" Відпочивати");
				break;
			}
			default:
				System.out.println("default");
			break;
		}
		
		
	}	
		
	}

